@st.cache_resource
def load_production_model(_torch, _build_model):
    """Load the cached production ShallowConvNet."""
    ckpt = _torch.load(MODEL_PATH, map_location="cpu", weights_only=False)
    model = _build_model(
        "shallow",
        n_channels=ckpt["n_channels"],
        n_times=ckpt["n_times"],
        n_classes=2,
    )
    model.load_state_dict(ckpt["state_dict"])
    model.eval()
    return model, ckpt["norm_mean"], ckpt["norm_std"], ckpt["n_channels"], ckpt["n_times"]


# ----- Prediction helper -----
def predict_epochs(epochs_array: np.ndarray, model, mean, std,
                   batch_size: int = 32):
    """Run model on (n_epochs, n_channels, n_times) array. Returns
    (predictions, probabilities)."""
    deps = get_deps()
    torch = deps["torch"]
    X = (epochs_array - mean[None, :, None]) / std[None, :, None]
    X = X.astype(np.float32)

    preds = []
    probs = []
    with torch.no_grad():
        for start in range(0, len(X), batch_size):
            batch = torch.from_numpy(X[start:start + batch_size]).unsqueeze(1)
            logits = model(batch)
            soft = torch.softmax(logits, dim=1).numpy()
            preds.append(np.argmax(soft, axis=1))
            probs.append(soft)
    return np.concatenate(preds), np.concatenate(probs, axis=0)


def show_results(epochs_array: np.ndarray, true_labels: np.ndarray | None,
                 model, mean, std):
    """Render per-trial prediction table and a confusion-matrix style summary."""
    preds, probs = predict_epochs(epochs_array, model, mean, std)

    LABEL_MAP = {0: "T1 (left fist)", 1: "T2 (right fist)"}
    rows = []
    for i, (p, pr) in enumerate(zip(preds, probs)):
        row = {
            "Trial": i + 1,
            "Predicted": LABEL_MAP[int(p)],
            "P(T1)": float(pr[0]),
            "P(T2)": float(pr[1]),
            "Confidence": float(pr.max()),
        }
        if true_labels is not None:
            row["True"] = LABEL_MAP[int(true_labels[i])]
            row["Correct"] = bool(int(p) == int(true_labels[i]))
        rows.append(row)
    result_df = pd.DataFrame(rows)

    # Summary metrics
    col1, col2, col3 = st.columns(3)
    col1.metric("Total trials", len(preds))
    col2.metric("Predicted T1",
                f"{(preds == 0).sum()} ({(preds == 0).mean():.1%})")
    col3.metric("Predicted T2",
                f"{(preds == 1).sum()} ({(preds == 1).mean():.1%})")

    if true_labels is not None:
        from sklearn.metrics import balanced_accuracy_score, confusion_matrix
        bacc = balanced_accuracy_score(true_labels, preds)
        st.metric("Balanced accuracy vs. event labels", f"{bacc:.3f}")
        cm = confusion_matrix(true_labels, preds, labels=[0, 1])
        cm_df = pd.DataFrame(
            cm,
            index=["True T1", "True T2"],
            columns=["Pred T1", "Pred T2"],
        )
        st.markdown("**Confusion matrix:**")
        st.dataframe(cm_df, use_container_width=False)

    # Format and render
    display_cols = ["Trial", "Predicted", "P(T1)", "P(T2)", "Confidence"]
    if true_labels is not None:
        display_cols += ["True", "Correct"]
    st.dataframe(
        result_df[display_cols].style.format({
            "P(T1)": "{:.3f}", "P(T2)": "{:.3f}", "Confidence": "{:.3f}",
        }),
        hide_index=True, use_container_width=True, height=400,
    )


